def div_by_primes_under(n):
    """
    >>> div_by_primes_under(10)(11)
    False
    >>> div_by_primes_under(10)(121)
    False
    >>> div_by_primes_under(10)(12)
    True
    >>> div_by_primes_under(5)(1)
    False
    """
    checker = lambda x: False
    i = 2 
    while i <= n:
        if not checker(i):
            checker = lambda k : k % i == 0
            if checker:
                return checker
        i = i + 1
    return False 


